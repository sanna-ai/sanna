# Filter out lone surrogates (Unicode category Cs); they are not valid in
# JSON, so canonical_json_bytes raises on them. They are out of scope for
# the canonicalization invariants we are testing.
json_safe_text = st.text(
    alphabet=st.characters(blacklist_categories=("Cs",)),
    max_size=200,
)

_json_primitives = st.one_of(
    st.none(),
    st.booleans(),
    st.integers(min_value=-(2**53) + 1, max_value=(2**53) - 1),
    json_safe_text,
)


def json_values():
    """Recursive JSON-compatible structures, depth <= 4."""
    return st.recursive(
        _json_primitives,
        lambda children: st.one_of(
            st.lists(children, max_size=8),
            st.dictionaries(
                st.text(
                    min_size=1,
                    max_size=20,
                    alphabet=st.characters(blacklist_categories=("Cs",)),
                ),
                children,
                max_size=8,
            ),
        ),
        max_leaves=20,
    )


# Minimal receipt dict for cv=6 (14-field formula).
# Pipe is banned from correlation_id per FIX-33.
# receipt_fingerprint and full_fingerprint are left empty because
# _verify_fingerprint_v013 computes and returns its own value independently
# of the stored receipt_fingerprint field.
_cv6_receipt_strategy = st.fixed_dictionaries({
    "correlation_id": st.text(
        alphabet=st.characters(
            blacklist_categories=("Cs",),
            blacklist_characters=("|",),
        ),
        max_size=50,
    ),
    "context_hash": st.just(EMPTY_HASH),
    "output_hash": st.just(EMPTY_HASH),
    "checks_version": st.just("6"),
    "checks": st.just([]),
    "parent_receipts": st.one_of(
        st.none(),
        st.lists(
            st.text(
                max_size=20,
                alphabet=st.characters(blacklist_categories=("Cs",)),
            ),
            max_size=3,
        ),
    ),
    "workflow_id": st.one_of(
        st.none(),
        st.text(
            max_size=30,
            alphabet=st.characters(blacklist_categories=("Cs",)),
        ),
    ),
    "receipt_fingerprint": st.just(""),
    "full_fingerprint": st.just(""),
})


def _make_base_receipt(cv: str) -> dict:
    """Build a minimal valid receipt dict for _verify_fingerprint_v013 at given cv."""
    base: dict = {
        "correlation_id": "test-pbt-corr",
        "context_hash": EMPTY_HASH,
        "output_hash": EMPTY_HASH,
        "checks_version": cv,
        "checks": [],
        "receipt_fingerprint": "",
        "full_fingerprint": "",
    }
    cv_int = int(cv)
    if cv_int >= 6:
        base["parent_receipts"] = None
        base["workflow_id"] = None
    if cv_int >= 8:
        base["enforcement_surface"] = "middleware"
        base["invariants_scope"] = "full"
    if cv_int >= 9:
        base["tool_name"] = "sanna"
    if cv_int >= 10:
        base["agent_identity"] = {"agent_session_id": "sn_ag_pbt_base"}
    return base


# ---- 1. Canonical JSON determinism ----

class TestCanonicalJsonDeterminism:
    """canonical_json_bytes is invariant under semantically-equivalent
    reordering of dict keys at any nesting level.

    Spec Section 3: Canonical JSON requires sort_keys=True at all depths.
    """

    @given(json_values())
    def test_dict_reordering_invariant(self, obj):
        """Reversed-key clone must produce byte-identical canonical JSON."""
        from copy import deepcopy

        def reverse_keys(o):
            if isinstance(o, dict):
                return {k: reverse_keys(v) for k, v in reversed(list(o.items()))}
            if isinstance(o, list):
                return [reverse_keys(e) for e in o]
            return o

        shuffled = reverse_keys(deepcopy(obj))
        assert canonical_json_bytes(obj) == canonical_json_bytes(shuffled), (
            "canonical_json_bytes must be invariant under dict key reordering "
            "at any nesting level"
        )


# ---- 2. NFC scope (ADR-004) ----

class TestNfcScope:
    """hash_text normalizes its input to NFC at the boundary, so NFC and
    NFD inputs hash identically. hash_obj does NOT normalize string values
    inside objects, so {k: NFC} and {k: NFD} differ in canonical JSON
    bytes and therefore in hash output.

    ADR-004: NFC Normalization Scope in Canonical JSON.
    See also tests/test_nfc_scope.py for fixture-based coverage.
    """

    @given(json_safe_text)
    def test_hash_text_nfc_invariant(self, s):
        """hash_text produces the same digest for NFC and NFD equivalents."""
        nfc = unicodedata.normalize("NFC", s)
        nfd = unicodedata.normalize("NFD", s)
        assert hash_text(nfc) == hash_text(nfd), (
            "hash_text must NFC-normalize at the boundary (ADR-004)"
        )

    # Known NFC/NFD-distinct example: e + combining acute accent -> e-acute.
    # The @example ensures the assertion executes at least once even if
    # Hypothesis does not happen to generate a combining character.
    @example("\xe9")
    @given(json_safe_text)
    def test_hash_obj_does_not_normalize_inside(self, s):
        """hash_obj must NOT NFC-normalize string values inside objects.

        ADR-004: NFC normalization is a hash_text boundary concern. Inside
        canonical_json_bytes, strings are preserved verbatim so that NFC
        and NFD representations remain distinguishable.
        """
        nfc = unicodedata.normalize("NFC", s)
        nfd = unicodedata.normalize("NFD", s)
        assume(nfc != nfd)
        assert hash_obj({"k": nfc}) != hash_obj({"k": nfd}), (
            "hash_obj must NOT NFC-normalize string values inside objects (ADR-004)"
        )


# ---- 3. Fingerprint stability ----

class TestFingerprintStability:
    """_verify_fingerprint_v013 produces the same computed fingerprint for
    the same input on repeated calls (3a) AND for inputs that survive
    a canonical-JSON round-trip unchanged (3b).

    3b catches subtle canonicalization bugs where parsing the canonical
    form back to a dict and re-computing changes the output. Note: there
    is no single centralized compute_fingerprint callable in the Python
    SDK; _verify_fingerprint_v013 is the authoritative re-computation
    path used by the offline verifier (see Property 10 for cross-site
    parity between emission and verification).
    """

    @given(_cv6_receipt_strategy)
    def test_deterministic_repeat(self, receipt):
        """Computed fingerprint is identical across two calls with the same dict."""
        _, fp1, _ = _verify_fingerprint_v013(receipt)
        _, fp2, _ = _verify_fingerprint_v013(receipt)
        assert fp1 == fp2, (
            "_verify_fingerprint_v013 must be deterministic on repeat calls"
        )

    @given(_cv6_receipt_strategy)
    def test_canonical_json_round_trip_stable(self, receipt):
        """Computed fingerprint is identical after canonical-JSON round-trip.

        The fingerprint formula hashes string fields (correlation_id, etc.)
        via hash_text and object/list fields via hash_obj. Both are pure
        functions of their inputs. A JSON round-trip preserves Python None
        (via JSON null) and string values, so the fingerprint must be stable.
        """
        round_tripped = json.loads(canonical_json_bytes(receipt).decode("utf-8"))
        _, fp_orig, _ = _verify_fingerprint_v013(receipt)
        _, fp_rt, _ = _verify_fingerprint_v013(round_tripped)
        assert fp_orig == fp_rt, (
            "computed fingerprint must be stable across canonical-JSON round-trip"
        )


# ---- 4. Empty container handling ----

class TestEmptyContainerHandling:
    """[] (empty array) and null (absent) MUST produce distinct computed
    fingerprints for container fields included in the formula.

    parent_receipts at cv>=6: None -> EMPTY_HASH, [] -> hash_obj([]).
    workflow_id at cv>=6: None -> EMPTY_HASH, non-empty str -> hash_text(str).
    These distinctions carry semantic meaning in the protocol.

    Note: hash_text("") == EMPTY_HASH (canonicalize_text("") returns ""
    and SHA-256 of b"" is the same sentinel). The empty-string/null
    distinction for workflow_id is therefore a code-path distinction
    only. This test uses a non-empty workflow_id to assert a meaningful
    hash difference.
    """

    def test_parent_receipts_empty_vs_null(self):
        """parent_receipts=[] and parent_receipts=None produce different fingerprints."""
        r_null = _make_base_receipt("6")
        r_null["parent_receipts"] = None

        r_empty = _make_base_receipt("6")
        r_empty["parent_receipts"] = []

        _, fp_null, _ = _verify_fingerprint_v013(r_null)
        _, fp_empty, _ = _verify_fingerprint_v013(r_empty)
        assert fp_null != fp_empty, (
            "parent_receipts=None and parent_receipts=[] must produce "
            "distinct fingerprints"
        )

    def test_workflow_id_non_empty_vs_null(self):
        """workflow_id='wf-001' and workflow_id=None produce different fingerprints.

        None -> EMPTY_HASH; 'wf-001' -> hash_text('wf-001') which is a
        different digest. The non-empty case is the semantically meaningful
        distinction in the protocol.
        """
        r_null = _make_base_receipt("6")
        r_null["workflow_id"] = None

        r_set = _make_base_receipt("6")
        r_set["workflow_id"] = "wf-001"

        _, fp_null, _ = _verify_fingerprint_v013(r_null)
        _, fp_set, _ = _verify_fingerprint_v013(r_set)
        assert fp_null != fp_set, (
            "workflow_id=None and workflow_id='wf-001' must produce "
            "distinct fingerprints"
        )

    def test_agent_identity_present_vs_absent_gates_cv10(self):
        """At cv=10, agent_identity absence causes the verifier to return empty.

        Spec section 2.19: agent_identity is required at cv=10. The verifier
        enforces this by returning (False, '', '') when agent_identity is
        absent or falsy. A valid cv=10 receipt with agent_identity produces
        a non-empty computed fingerprint.
        """
        r_with = _make_base_receipt("10")

        r_without = dict(r_with)
        del r_without["agent_identity"]

        _, fp_with, _ = _verify_fingerprint_v013(r_with)
        ok_without, fp_without, _ = _verify_fingerprint_v013(r_without)

        assert fp_with != "", (
            "cv=10 receipt with agent_identity should produce a non-empty fingerprint"
        )
        assert fp_without == "", (
            "cv=10 receipt missing agent_identity should return empty computed "
            "fingerprint per spec section 2.19 verifier enforcement"
        )


# ---- 5. Float handling ----

class TestFloatHandling:
    """Per spec Section 3: integer-valued floats (1.0) coerce to int (1)
    before canonicalization; non-integer floats (1.5) are rejected.
    """

    @given(st.integers(min_value=-(2**53) + 1, max_value=(2**53) - 1))
    def test_integer_valued_float_coerces_to_int(self, n):
        """float(n) and n must produce byte-identical canonical JSON."""
        assert canonical_json_bytes(float(n)) == canonical_json_bytes(n), (
            "integer-valued floats must coerce to int before canonicalization"
        )

    @given(
        st.floats(min_value=-1e6, max_value=1e6, allow_nan=False, allow_infinity=False)
    )
    def test_non_integer_float_rejected(self, f):
        """Non-integer floats must raise ValueError from canonical_json_bytes."""
        assume(f != int(f))
        with pytest.raises(ValueError, match="Non-integer float"):
            canonical_json_bytes(f)


