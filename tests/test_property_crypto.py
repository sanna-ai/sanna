# ---- 7. Signature round-trip ----

class TestSignatureRoundTrip:
    """sign_bytes + verify_signature round-trip on arbitrary bytes.
    Tampered message AND tampered signature both fail verification.

    Keypair is generated once per test class (scope='class') so that the
    example budget is spent on signing variation, not key generation.

    NOTE: receipt-level signing (sign_receipt / verify_receipt_signature)
    is covered by fixture tests in test_crypto_integrity.py and is
    intentionally not duplicated here. PBT here targets the primitive.
    """

    @pytest.fixture(scope="class")
    def keypair(self):
        from cryptography.hazmat.primitives.asymmetric.ed25519 import Ed25519PrivateKey
        private_key = Ed25519PrivateKey.generate()
        public_key = private_key.public_key()
        return private_key, public_key

    @given(st.binary(min_size=1, max_size=10_000))
    def test_sign_verify_round_trip(self, keypair, msg):
        """Signing then verifying any byte sequence returns True."""
        private_key, public_key = keypair
        sig_b64 = sign_bytes(msg, private_key)
        assert verify_signature(msg, sig_b64, public_key) is True, (
            "verify_signature must return True for a freshly signed message"
        )

    @given(msg=st.binary(min_size=1, max_size=10_000), data=st.data())
    def test_tampered_message_fails(self, keypair, msg, data):
        """Flipping one bit in the plaintext must fail verification."""
        private_key, public_key = keypair
        sig_b64 = sign_bytes(msg, private_key)
        byte_idx = data.draw(st.integers(min_value=0, max_value=len(msg) - 1))
        bit_idx = data.draw(st.integers(min_value=0, max_value=7))
        tampered = bytearray(msg)
        tampered[byte_idx] ^= (1 << bit_idx)
        assert verify_signature(bytes(tampered), sig_b64, public_key) is False, (
            "verify_signature must reject a tampered message"
        )

    @given(msg=st.binary(min_size=1, max_size=10_000), data=st.data())
    def test_tampered_signature_fails(self, keypair, msg, data):
        """Flipping one bit in the Ed25519 signature must fail verification.

        sign_bytes returns a base64-encoded 64-byte Ed25519 signature.
        Decode to raw bytes, tamper one bit, re-encode to base64.
        """
        private_key, public_key = keypair
        sig_b64 = sign_bytes(msg, private_key)
        sig_bytes = base64.b64decode(sig_b64)  # Ed25519 signature: always 64 bytes
        byte_idx = data.draw(st.integers(min_value=0, max_value=len(sig_bytes) - 1))
        bit_idx = data.draw(st.integers(min_value=0, max_value=7))
        tampered = bytearray(sig_bytes)
        tampered[byte_idx] ^= (1 << bit_idx)
        tampered_b64 = base64.b64encode(bytes(tampered)).decode("ascii")
        assert verify_signature(msg, tampered_b64, public_key) is False, (
            "verify_signature must reject a tampered Ed25519 signature"
        )


# ---- 8. Field count dispatch (external observation) ----

class TestFieldCountDispatch:
    """The cv field determines which receipt fields are hashed into the
    fingerprint. Tested by constructing pairs of receipts that differ
    ONLY in a field that is inside vs outside the cv-specific range:

    cv=5 (12 fields): parent_receipts is EXCLUDED.
      Receipts differing only in parent_receipts produce the SAME fingerprint.
    cv=6 (14 fields): parent_receipts IS included.
      Receipts differing only in parent_receipts produce DIFFERENT fingerprints.
    cv=7 (14 fields, same as cv=6): enforcement_surface is EXCLUDED.
      Receipts differing only in enforcement_surface produce the SAME fingerprint.
    cv=8 (16 fields): enforcement_surface IS included.
      Receipts differing only in enforcement_surface produce DIFFERENT fingerprints.
    cv=9 (20 fields): agent_identity is EXCLUDED.
      Adding agent_identity to the receipt dict does NOT change the fingerprint.
    cv=10 (21 fields): agent_identity IS included.
      Receipts differing only in agent_identity produce DIFFERENT fingerprints.

    Cite spec Section 4 for the cv/field-numbering.
    """

    def test_cv5_excludes_field_13_parent_receipts(self):
        """At cv=5, parent_receipts is outside the 12-field formula.

        Two cv=5 receipts identical in fields 1-12 but differing in
        parent_receipts must produce the SAME computed fingerprint.
        """
        r_null = _make_base_receipt("5")
        r_null["parent_receipts"] = None

        r_with = dict(r_null)
        r_with["parent_receipts"] = ["r_abc_001"]

        _, fp_null, _ = _verify_fingerprint_v013(r_null)
        _, fp_with, _ = _verify_fingerprint_v013(r_with)
        assert fp_null == fp_with, (
            "at cv=5, parent_receipts must NOT affect the fingerprint "
            "(field 13 is outside the 12-field formula)"
        )

    def test_cv6_includes_field_13_parent_receipts(self):
        """At cv=6, parent_receipts is field 13 (inside the 14-field formula).

        Two cv=6 receipts identical in fields 1-12 but differing in
        parent_receipts must produce DIFFERENT computed fingerprints.
        """
        r_null = _make_base_receipt("6")
        r_null["parent_receipts"] = None

        r_with = dict(r_null)
        r_with["parent_receipts"] = ["r_abc_001"]

        _, fp_null, _ = _verify_fingerprint_v013(r_null)
        _, fp_with, _ = _verify_fingerprint_v013(r_with)
        assert fp_null != fp_with, (
            "at cv=6, parent_receipts must affect the fingerprint "
            "(field 13 is inside the 14-field formula)"
        )

    def test_cv7_excludes_field_15_enforcement_surface(self):
        """At cv=7, enforcement_surface is outside the 14-field formula.

        Two cv=7 receipts differing only in enforcement_surface must
        produce the SAME computed fingerprint.
        """
        r_mid = _make_base_receipt("7")
        r_mid["enforcement_surface"] = "middleware"

        r_gw = dict(r_mid)
        r_gw["enforcement_surface"] = "gateway"

        _, fp_mid, _ = _verify_fingerprint_v013(r_mid)
        _, fp_gw, _ = _verify_fingerprint_v013(r_gw)
        assert fp_mid == fp_gw, (
            "at cv=7, enforcement_surface must NOT affect the fingerprint "
            "(field 15 is outside the 14-field formula)"
        )

    def test_cv8_includes_field_15_enforcement_surface(self):
        """At cv=8, enforcement_surface is field 15 (inside the 16-field formula).

        Two cv=8 receipts differing only in enforcement_surface must
        produce DIFFERENT computed fingerprints.
        """
        r_mid = _make_base_receipt("8")
        r_mid["enforcement_surface"] = "middleware"

        r_gw = dict(r_mid)
        r_gw["enforcement_surface"] = "gateway"

        _, fp_mid, _ = _verify_fingerprint_v013(r_mid)
        _, fp_gw, _ = _verify_fingerprint_v013(r_gw)
        assert fp_mid != fp_gw, (
            "at cv=8, enforcement_surface must affect the fingerprint "
            "(field 15 is inside the 16-field formula)"
        )

    def test_cv9_excludes_field_21_agent_identity(self):
        """At cv=9, agent_identity is absent (field 21 does not exist).

        Adding an agent_identity key to a cv=9 receipt dict does NOT
        change the computed fingerprint because the 20-field formula
        does not include it.
        """
        r_no_ai = _make_base_receipt("9")
        r_with_ai = dict(r_no_ai)
        r_with_ai["agent_identity"] = {"agent_session_id": "sn_ag_extra"}

        _, fp_no_ai, _ = _verify_fingerprint_v013(r_no_ai)
        _, fp_with_ai, _ = _verify_fingerprint_v013(r_with_ai)

        assert fp_no_ai != "", (
            "cv=9 receipt should produce a non-empty computed fingerprint"
        )
        assert fp_no_ai == fp_with_ai, (
            "at cv=9, agent_identity must NOT affect the fingerprint "
            "(field 21 exists only in the cv=10 formula)"
        )

    def test_cv10_includes_field_21_agent_identity(self):
        """At cv=10, agent_identity is field 21 (inside the 21-field formula).

        Two cv=10 receipts differing only in agent_identity must produce
        DIFFERENT computed fingerprints.
        """
        r_a = _make_base_receipt("10")
        r_a["agent_identity"] = {"agent_session_id": "sn_ag_alice_001"}

        r_b = dict(r_a)
        r_b["agent_identity"] = {"agent_session_id": "sn_ag_bob_001"}

        _, fp_a, _ = _verify_fingerprint_v013(r_a)
        _, fp_b, _ = _verify_fingerprint_v013(r_b)
        assert fp_a != fp_b, (
            "at cv=10, agent_identity must affect the fingerprint "
            "(field 21 is inside the 21-field formula)"
        )


# ---- 9. Redaction marker shape ----

class TestRedactionMarkerShape:
    """Per spec section 2.11.1, a redaction marker is exactly:
        {__redacted__: True, original_hash: <sha256-hex>}
    where original_hash is the SHA-256 hex digest of
    NFC(original_value).encode('utf-8') -- the full 64-hex digest.

    _make_redaction_marker is the canonical construction function.
    Its output must be byte-stable across logically equivalent inputs
    that differ only in NFC/NFD encoding.
    """

    @given(json_safe_text)
    def test_marker_structure(self, s):
        """_make_redaction_marker always returns the correct marker shape."""
        marker = _make_redaction_marker(s)
        assert marker.get("__redacted__") is True, (
            "marker must have __redacted__ = True"
        )
        h = marker.get("original_hash")
        assert isinstance(h, str), "original_hash must be a string"
        assert len(h) == 64, "original_hash must be a full 64-hex SHA-256 digest"
        assert all(c in "0123456789abcdef" for c in h), (
            "original_hash must be lowercase hex with no prefix"
        )

    @given(json_safe_text)
    def test_marker_hash_is_nfc_sha256(self, s):
        """original_hash equals SHA-256 of NFC(s).encode('utf-8')."""
        marker = _make_redaction_marker(s)
        nfc_s = unicodedata.normalize("NFC", s)
        expected = hashlib.sha256(nfc_s.encode("utf-8")).hexdigest()
        assert marker["original_hash"] == expected, (
            "original_hash must be SHA-256 of the NFC-normalized input"
        )

    @given(json_safe_text)
    def test_nfc_nfd_produce_same_marker_hash(self, s):
        """NFC and NFD forms of the same string produce the same marker hash.

        _make_redaction_marker normalizes to NFC before hashing, consistent
        with the hash_text NFC-normalization boundary in ADR-004.
        """
        nfc_s = unicodedata.normalize("NFC", s)
        nfd_s = unicodedata.normalize("NFD", s)
        marker_nfc = _make_redaction_marker(nfc_s)
        marker_nfd = _make_redaction_marker(nfd_s)
        assert marker_nfc["original_hash"] == marker_nfd["original_hash"], (
            "_make_redaction_marker must produce the same hash for "
            "NFC and NFD equivalents"
        )


# ---- 10. Fingerprint cross-site parity ----

class TestFingerprintCrossSiteParity:
    """Receipts generated by generate_receipt() pass verify_fingerprint().

    There is no single centralized compute_fingerprint callable in the
    Python SDK; the fingerprint formula is implemented inline in
    receipt.py (generate_receipt), middleware.py (_build_receipt_dict),
    and redaction.py (_apply_redaction_markers). The canonical
    re-computation path is verify.py (_verify_fingerprint_v013).

    This test uses the sample-input equality fallback: a receipt emitted
    by generate_receipt() must pass offline verification via
    verify_fingerprint(). If emission and verification have drifted to
    different formulas, the fingerprint mismatch surfaces here.

    Receipt-level signing is not exercised here (covered by
    test_crypto_integrity.py). Only fingerprint parity is tested.
    """

    def _make_trace(self, correlation_id: str) -> dict:
        return {
            "correlation_id": correlation_id,
            "observations": [],
            "output": None,
            "input": {},
        }

    def test_cv9_receipt_fingerprint_verifies(self):
        """cv=9 receipt from generate_receipt() passes verify_fingerprint()."""
        from sanna.receipt import generate_receipt, receipt_to_dict
        from sanna.verify import verify_fingerprint

        trace = self._make_trace("pbt-cv9-parity-001")
        receipt_obj = generate_receipt(trace)
        receipt_dict = receipt_to_dict(receipt_obj)

        matches, computed, expected = verify_fingerprint(receipt_dict)
        assert matches, (
            f"generate_receipt() produced a cv=9 receipt whose fingerprint "
            f"does not verify: computed={computed!r}, expected={expected!r}"
        )

    def test_cv10_receipt_fingerprint_verifies(self):
        """cv=10 receipt (with agent_identity) from generate_receipt() passes verify_fingerprint()."""
        from sanna.receipt import generate_receipt, receipt_to_dict
        from sanna.verify import verify_fingerprint

        trace = self._make_trace("pbt-cv10-parity-001")
        receipt_obj = generate_receipt(
            trace,
            enforcement_surface="gateway",
            invariants_scope="full",
            agent_identity={"agent_session_id": "sn_ag_pbt_parity_test"},
        )
        receipt_dict = receipt_to_dict(receipt_obj)

        matches, computed, expected = verify_fingerprint(receipt_dict)
        assert matches, (
            f"generate_receipt() produced a cv=10 receipt whose fingerprint "
            f"does not verify: computed={computed!r}, expected={expected!r}"
        )

    def test_full_and_short_fingerprint_both_verify(self):
        """verify_fingerprint checks both full_fingerprint (64-hex) and
        receipt_fingerprint (16-hex truncation). Formula drift between
        emission and verification would surface as a mismatch in either.
        """
        from sanna.receipt import generate_receipt, receipt_to_dict
        from sanna.verify import _verify_fingerprint_v013

        trace = self._make_trace("pbt-full-short-parity-001")
        receipt_obj = generate_receipt(trace)
        receipt_dict = receipt_to_dict(receipt_obj)

        matches, computed_short, stored_short = _verify_fingerprint_v013(receipt_dict)

        assert matches, (
            "full_fingerprint and receipt_fingerprint must both verify for a "
            "freshly generated cv=9 receipt"
        )
        assert computed_short == stored_short
        assert computed_short == receipt_dict["receipt_fingerprint"]
